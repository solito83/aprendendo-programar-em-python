nasa = input('Digite algo: ')
print('O tipo primitivo de nasa é',type(nasa))
print('nasa só tem espaço',nasa.isspace())
print('nasa: é um número?',nasa.isnumeric())
print('nasa é alfanumerico?',nasa.isalnum())
print('nasa é alfabetico?',nasa.isalpha())
print('nasa está em maisculo?',nasa.isupper())
print('nasa está em minusculo?',nasa.islower())
print('nasa está capitalizada?',nasa.istitle())

